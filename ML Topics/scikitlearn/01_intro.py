# 2. Understanding Machine Learning Basics
# Machine learning can be broadly classified into:

# Supervised Learning: The model is trained on labeled data.
# Examples: Linear Regression, Logistic Regression, Support Vector Machines (SVM), Random Forest.

# Unsupervised Learning: The model is trained on unlabeled data to find hidden patterns.
# Examples: K-Means Clustering, Principal Component Analysis (PCA).

# Reinforcement Learning: The model learns by interacting with an environment and receiving feedback through rewards.